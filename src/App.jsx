function fmtET(fx) {
  const dt = new Date(fx.kickoff);
  const day = dt.toLocaleString("en-US", { weekday: "short", month: "short", day: "numeric", timeZone: "America/New_York" });
  if (fx.tba) return `${day} · Time TBA`;
  const time = dt.toLocaleString("en-US", { hour: "numeric", minute: "2-digit", hour12: true, timeZone: "America/New_York" });
  return `${day} · ${time} ET`;
}

