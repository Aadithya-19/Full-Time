/* ================= SCORING =================
   +5 correct outcome · +0.5 per correct team goal count · −1 per wrong one
   (predict 2–2, real 0–0 → 5 − 1 − 1 = 3). Goal component toggleable by admin. */
function outcomeOf(h, a) { return h > a ? "H" : h < a ? "A" : "D"; }
function scorePrediction(pred, result, goalScoring) {
  if (!pred || !result) return null;
  const hasGoals = Number.isInteger(pred.h) && Number.isInteger(pred.a);
  const pick = hasGoals ? outcomeOf(pred.h, pred.a) : pred.pick;
  if (!pick) return null;
  const actual = outcomeOf(result.h, result.a);
  let pts = pick === actual ? 5 : 0;
  let exact = false;
  if (goalScoring && hasGoals) {
    pts += pred.h === result.h ? 0.5 : -1;
    pts += pred.a === result.a ? 0.5 : -1;
    exact = pred.h === result.h && pred.a === result.a;
  }
  return { pts, correctOutcome: pick === actual, exact };
}
function computeStats(preds, fixtures, goalScoring) {
  let pts = 0, correct = 0, exact = 0, played = 0;
  fixtures.filter((f) => f.final && f.result).forEach((f) => {
    const s = scorePrediction(preds?.[f.id], f.result, goalScoring);
    if (s) { pts += s.pts; played++; if (s.correctOutcome) correct++; if (s.exact) exact++; }
  });
  return { pts: Math.round(pts * 10) / 10, correct, exact, played };
}

/* ================= STORAGE + HASH =================
   Two-tier persistence:
   1) Firestore (Google Cloud) — the durable, shared, cross-device store. The browser never talks
      to Firestore or holds any API key — it calls this app's own `/api/db` serverless function
      (see /api/db.js), which holds the Firebase key as a server-side environment variable and
      proxies the request. Nothing Firebase-related ships in the client bundle or the repo.
   2) Built-in artifact storage (window.storage) — a local cache + fallback used only when the
      proxy is unreachable (e.g. running as a Claude.ai artifact preview with no /api routes, or
      offline). Every call to it is wrapped in try/catch, so it silently no-ops when unavailable. */
async function fsGet(key, shared) {
  try {
    const res = await fetch(`/api/db?action=get&shared=${shared ? "true" : "false"}&key=${encodeURIComponent(key)}`);
    if (!res.ok) return undefined;
    const { value } = await res.json();
    return value == null ? undefined : JSON.parse(value);
  } catch { return undefined; } // no /api route reachable (e.g. artifact preview) — caller falls back to window.storage
}
async function fsSet(key, val, shared) {
  try {
    const res = await fetch(`/api/db`, {
      method: "POST", headers: { "Content-Type": "application/json" },
      body: JSON.stringify({ shared: !!shared, key, value: val }),
    });
    if (!res.ok) return false;
    const d = await res.json();
    return !!d.ok;
  } catch { return false; }
}
async function fsListKeys(prefix, shared) {
  try {
    const res = await fetch(`/api/db?action=list&shared=${shared ? "true" : "false"}&prefix=${encodeURIComponent(prefix || "")}`);
    if (!res.ok) return undefined;
    const { keys } = await res.json();
    return keys;
  } catch { return undefined; }
}

async function sget(key, shared, fallback) {
  const remote = await fsGet(key, shared);
  if (remote !== undefined) {
    try { await window.storage.set(key, JSON.stringify(remote), shared); } catch {} // refresh local cache
    return remote;
  }
  try { const r = await window.storage.get(key, shared); return r ? JSON.parse(r.value) : fallback; }
  catch { return fallback; }
}
async function sset(key, val, shared) {
  const remoteOk = await fsSet(key, val, shared);
  let localOk = false;
  try { await window.storage.set(key, JSON.stringify(val), shared); localOk = true; } catch {}
  return remoteOk || localOk;
}
async function slistKeys(prefix, shared) {
  const remote = await fsListKeys(prefix, shared);
  if (remote !== undefined) return remote;
  try { const r = await window.storage.list(prefix, shared); return r?.keys || []; } catch { return []; }
}

/* Login session is a per-browser concern — keep it in localStorage so it survives refreshes
   instantly and never depends on a server round-trip. Falls back to an in-memory value if
   localStorage is unavailable (e.g. a locked-down artifact sandbox). */
let _memSession = null;
function loadSession() {
  try { const v = localStorage.getItem("ftl:session"); return v ? JSON.parse(v) : null; }
  catch { return _memSession; }
}
function saveSession(obj) {
  _memSession = obj;
  try {
    if (obj == null) localStorage.removeItem("ftl:session");
    else localStorage.setItem("ftl:session", JSON.stringify(obj));
  } catch {}
}
async function hash(text) {
  try {
    const buf = await crypto.subtle.digest("SHA-256", new TextEncoder().encode("plp-salt::" + text));
    return [...new Uint8Array(buf)].map((b) => b.toString(16).padStart(2, "0")).join("");
  } catch {
    let h = 5381; const s = "plp-salt::" + text;
    for (let i = 0; i < s.length; i++) h = ((h << 5) + h + s.charCodeAt(i)) | 0;
    return "djb2-" + (h >>> 0).toString(16);
  }
}

