/* ================= PL TABLE ================= */
function buildTable(fixtures, comp) {
  const t = {};
  const row = (n) => (t[n] = t[n] || { team: n, p: 0, w: 0, d: 0, l: 0, gf: 0, ga: 0 });
  (TEAMS_BY_COMP[comp] || []).forEach(row);
  fixtures.filter((f) => f.comp === comp && f.final && f.result).forEach((f) => {
    const h = row(f.home), a = row(f.away), r = f.result;
    h.p++; a.p++; h.gf += r.h; h.ga += r.a; a.gf += r.a; a.ga += r.h;
    if (r.h > r.a) { h.w++; a.l++; } else if (r.h < r.a) { a.w++; h.l++; } else { h.d++; a.d++; }
  });
  return Object.values(t).map((r) => ({ ...r, gd: r.gf - r.ga, pts: r.w * 3 + r.d }))
    .sort((x, y) => y.pts - x.pts || y.gd - x.gd || y.gf - x.gf || x.team.localeCompare(y.team));
}
